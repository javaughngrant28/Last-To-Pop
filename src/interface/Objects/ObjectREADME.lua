-- # Understanding Objects

-- An **object** is like a box that holds information (properties) and actions (methods).  

-- ## Example: A Roblox Part Object  

-- **Properties (Information it stores):**  
-- - Size (how big it is)  
-- - Color (what color it is)  
-- - Position (where it is in the world)  

-- **Methods (Actions it can do):**  
-- - Move() → Changes its position  
-- - Destroy() → Removes it from the game  

-- Objects help organize things so code is neat and easy to use!
